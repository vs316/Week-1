package practiceprograms;

public class SelfProblems {
    static double SimpleInterest(double p, double r, double t){
        return (p*r*t)/100;
    }
    static float PeriRect(float l, float b){
        return 2*(l+b);
    }
    static double Power(double base, double exponent){
        return Math.pow(base, exponent);
    }
    static double Average(float a, float b, float c){
        return (a+b+c)/3;
    }
    static double KmsToMiles(double distance){
        return distance * 0.621371;
    }
}
